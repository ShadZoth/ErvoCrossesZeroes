/**
 * Values that can be placed in the section.
 *
 * @author Ervo Victor
 * @since 18.03.14
 */
public enum SectionValue {
    /**
     * Empty section.
     */
    EMPTY,

    /**
     * Crossed section.
     */
    CROSS,

    /**
     * Zeroed section.
     */
    ZERO
}
